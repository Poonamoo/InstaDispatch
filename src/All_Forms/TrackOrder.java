    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
pkgid = Integer.parseInt(reflbl.getText());
int sel;        

if(statuslbl.getText().equals("Pending")) {
 sel=JOptionPane.showConfirmDialog(null, "Do You Want to cancel this Order!");
    if(sel==0){
        cancelOrder();
    }
}else if(statuslbl.getText().equals("pickup") || statuslbl.getText().equals("In Transit")){
    sel=JOptionPane.showConfirmDialog(null, "Your Order Is " + statuslbl.getText() + " Do You Still Want to cancel Order!");
    if(sel==0){
        cancelOrder();
    }
}
    }//GEN-LAST:event_jButton3ActionPerformed

