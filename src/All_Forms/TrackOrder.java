    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
getdata();
setstat();
String stat = statuslbl.getText();
switch(stat){
    case "Delivered":delcb.setEnabled(true);
    case "Out for Delivery": outcb.setEnabled(true);
    case "Reached Destination": reachcb.setEnabled(true);
    case "In Transit": trancb.setEnabled(true);
    case "pickup": pickcb.setEnabled(true);
    case "Pending": bcb.setEnabled(true);
    break;
    default :delcb.setEnabled(false);
             outcb.setEnabled(false);
             reachcb.setEnabled(false);
             trancb.setEnabled(false);
             pickcb.setEnabled(false);
             bcb.setEnabled(false);
}
    }//GEN-LAST:event_jButton1ActionPerformed

